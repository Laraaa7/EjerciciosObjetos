package Herencia;

public class Persona {
	
	//variables
	private String nombre;
	private int edad;
	
	//constructor
	public Persona(String nombre, int edad) {
		this.nombre=nombre;
		this.edad=edad;
	}
	//getters
	public String getNombre() {
		return nombre;
	}
	public int getEdad() {
		return edad;
	}
}

	